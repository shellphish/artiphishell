# EXTRACT ALL CORPUS ARCHIVES
find . -type f \( -name "*corpus*.tar*" -o -name "*corpus*.zip" -o -name "*corpus*.rar" -o -name "*corpus*.7z" -o -name "*corpus*.tgz" -o -name "*corpus*.tbz2" -o -name "*corpus*.txz" \) -exec bash -c '
    for file; do
        dir=$(dirname "$file")
        base=$(basename "$file" | sed -e "s/\.tar\.[^.]*$//" -e "s/\.[^.]*$//")
        extract_dir="$dir/$base"

        echo "Processing: $file"
        rm -rf "$extract_dir" 2>/dev/null
        mkdir -p "$extract_dir" 2>/dev/null
        
        case "$file" in
            *.tar.gz|*.tgz) timeout 600 tar -xzf "$file" -C "$extract_dir" 2>/dev/null ;;
            *.tar.bz2|*.tbz2) timeout 600 tar -xjf "$file" -C "$extract_dir" 2>/dev/null ;;
            *.tar.xz|*.txz) timeout 600 tar -xJf "$file" -C "$extract_dir" 2>/dev/null ;;
            *.tar) timeout 600 tar -xf "$file" -C "$extract_dir" 2>/dev/null ;;
            *.zip) timeout 600 unzip -q "$file" -d "$extract_dir" 2>/dev/null ;;
            *.rar) timeout 600 unrar x "$file" "$extract_dir/" 2>/dev/null ;;
            *.7z) timeout 600 7z x "$file" -o"$extract_dir" 2>/dev/null ;;
        esac
        echo "Done: $file"
    done
' bash {} +

