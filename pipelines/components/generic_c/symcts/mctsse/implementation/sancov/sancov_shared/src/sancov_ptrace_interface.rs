pub fn is_sancov_syscall(sysno: i64) -> bool {
    (0x133700..0x133800).contains(&sysno)
}
pub const SYSCALL_SANCOV_TRACE_PC: i64 = 0x133700;
pub const SYSCALL_SANCOV_TRACE_PC_GUARD: i64 = 0x133701;
pub const SYSCALL_SANCOV_TRACE_PC_INDIRECT: i64 = 0x133702;

pub const SYSCALL_SANCOV_CAPTURE_PC_TABLE: i64 = 0x133708;
pub const SYSCALL_SANCOV_CAPTURE_PC_GUARDS: i64 = 0x133709;
pub const SYSCALL_SANCOV_CAPTURE_INLINE_BOOLS: i64 = 0x13370A;
pub const SYSCALL_SANCOV_CAPTURE_INLINE_8BIT_COUNTERS: i64 = 0x13370B;
